package br.calc;

/**
 *
 * @author utfpr
 */
public class Calculadora {
    public int somar(int a, int b){
        return a + b;
    }
    
    public float multiplica(int a, int b) {
        return a * b;
    }
    
    public int subtrair(int a, int b) {
        return a - b;
    }
    
    public float dividir(int a, int b) {
        return a / b;
    }
}
